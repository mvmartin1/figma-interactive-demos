#!/usr/bin/env node
import { readFile, writeFile, mkdir, access } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..', '..');
const distHtml = resolve(root, 'dist', 'index.html');
const outFile = resolve(__dirname, '..', 'src', 'demo-html.ts');

try {
  await access(distHtml);
} catch {
  console.error(`[bundle-html] Missing ${distHtml}. Run \`npm run build\` at the repo root first.`);
  process.exit(1);
}

let html = await readFile(distHtml, 'utf8');

// Two transforms needed for WebView compatibility:
// 1. WebView's source={{ html }} on iOS rejects <script type="module" crossorigin>.
//    vite-plugin-singlefile inlines everything so the script body has no top-level
//    import/export — safe to downgrade to a classic <script>.
// 2. The plugin emits the script in <head>, so a classic script runs BEFORE the
//    body is parsed and React's createRoot can't find #root. Move it to the end
//    of <body> so the DOM exists when it executes.
const scriptRe = /<script\s+type="module"(?:\s+crossorigin)?\s*>([\s\S]*?)<\/script>/;
const match = html.match(scriptRe);
if (!match) {
  console.error('[bundle-html] Could not find the expected <script type="module"> tag in dist/index.html');
  process.exit(1);
}
html = html.replace(scriptRe, '');

// Inject a visible error overlay before the app script so crashes show on screen
const errorOverlay = `<script>
(function(){
  var el=null;
  function show(msg){
    if(!el){
      el=document.createElement('div');
      el.style.cssText='position:fixed;top:0;left:0;right:0;bottom:0;background:#fff;z-index:99999;padding:24px;font-family:monospace;overflow:auto;';
      el.innerHTML='<h2 style="color:#c00;margin:0 0 12px">JS Error</h2>';
      document.body.appendChild(el);
    }
    var p=document.createElement('pre');
    p.style.cssText='color:#c00;font-size:12px;white-space:pre-wrap;margin:0 0 8px;';
    p.textContent=msg;
    el.appendChild(p);
  }
  window.addEventListener('error',function(e){show(e.message+'\\n'+e.filename+':'+e.lineno);});
  window.addEventListener('unhandledrejection',function(e){show('Unhandled: '+String(e.reason));});
})();
</script>`;

// Add a loading indicator inside #root — React replaces it on mount, so
// if the page stays blank but shows "Loading…" React never ran.
// Use a function as the replacement to prevent $ in the JS bundle from being
// interpreted as special replacement patterns ($& $' $` etc).
html = html.replace('<div id="root"></div>', () =>
  '<div id="root"><div style="position:fixed;top:50%;left:50%;transform:translate(-50%,-50%);font-family:monospace;font-size:15px;color:#555">Loading…</div></div>',
);

const appScript = `<script>${match[1]}<\/script>`;
html = html.replace('</body>', () => errorOverlay + appScript + '</body>');

const escaped = JSON.stringify(html);

await mkdir(dirname(outFile), { recursive: true });
await writeFile(outFile, `// Auto-generated by scripts/bundle-html.mjs. Do not edit.\nexport const demoHtml: string = ${escaped};\n`);

const sizeKb = (Buffer.byteLength(html, 'utf8') / 1024).toFixed(1);
console.log(`[bundle-html] Wrote ${outFile} (${sizeKb} KB)`);
